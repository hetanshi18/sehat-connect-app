with open(os.path.join(MODEL_DIR, "symptom_vocab.json")) as f:
    VOCAB = set(json.load(f))

def canonicalize_list(symptoms_raw):
    """Normalize a list of symptom strings into vocab-aligned tokens."""
    canon = []
    for s in symptoms_raw:
        s0 = normalize_symptom(s)
        if s0 is None:
            continue
        # exact in vocab
        if s0 in VOCAB:
            canon.append(s0)
            continue
        # light fuzzy match if not found (threshold can be tuned)
        match, score, _ = fuzzprocess.extractOne(s0, VOCAB, scorer=fuzz.token_sort_ratio)
        if score >= 90:
            canon.append(match)
        else:
            # keep as-is only if you plan to expand vocab; otherwise drop
            # canon.append(s0)
            pass
    return sorted(set(canon))

def vectorize(symptoms_norm):
    """Transform canonical symptoms to multi-hot vector with the saved binarizer."""
    mlb = joblib.load(os.path.join(MODEL_DIR, "symptom_binarizer.pkl"))
    return mlb.transform([symptoms_norm])

DISEASE_PRIORS = {
    "Common Cold": 0.25,      # extremely common
    "Flu": 0.15,              # seasonal, quite common
    "Allergy": 0.12,          # very common
    "Migraine": 0.10,         # common neurological condition
    "Hypertension": 0.08,     # chronic, but not usually symptom-driven
    "Diabetes": 0.06,         # chronic
    "Asthma": 0.05,
    "Sinusitis": 0.04,
    "Bronchitis": 0.03,
    "COVID-19": 0.02,         # currently less frequent than peak pandemic
    "Pneumonia": 0.02,
    "Tuberculosis": 0.015,
    "Malaria": 0.015,
    "Dengue": 0.015,
    "Jaundice": 0.01,
    "Hepatitis": 0.01,
    "Chickenpox": 0.005,
    "Measles": 0.003,
    "Mumps": 0.003,
    "Rubella": 0.002,
    "Scarlet Fever": 0.001,
    "Tonsillitis": 0.01
}


def predict_disease(symptoms, top_k=3):
    # Step 1: canonicalize input
    canonical_symptoms = canonicalize_list(symptoms)

    # Step 2: vectorize into multi-hot
    Xb = vectorize(canonical_symptoms)

    # Step 3: predict probabilities
    probs = winner.predict_proba(Xb)[0]

    # Step 4: get disease labels
    diseases = le.classes_

    # Step 5: apply priors
    adjusted = []
    for disease, prob in zip(diseases, probs):
        prior = DISEASE_PRIORS.get(disease, 0.05)
        adjusted_prob = prob * prior
        adjusted.append((disease, adjusted_prob))

    # Step 6: normalize to sum=1
    total = sum(p for _, p in adjusted)
    adjusted = [(d, p / total) for d, p in adjusted]

    # Step 7: sort & return top_k
    adjusted.sort(key=lambda x: x[1], reverse=True)
    predictions = [{"disease": d, "prob": float(p)} for d, p in adjusted[:top_k]]

    return {
        "canonical_symptoms": canonical_symptoms,
        "predictions": predictions
    }

