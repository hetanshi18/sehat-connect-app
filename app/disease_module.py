# Top-K (k=3) accuracy
def topk_accuracy(model, Xb, y_true, k=3):
    # need predict_proba; if not available, use decision_function->softmax-ish
    if hasattr(model, "predict_proba"):
        P = model.predict_proba(Xb)
    else:
        # CalibratedClassifierCV exposes predict_proba; LinearSVC alone would not.
        # If we had decision_function only, we could softmax it. But here we ensured calibration.
        raise RuntimeError("Model has no predict_proba; ensure calibration or pick prob-capable model.")
    topk = np.argsort(P, axis=1)[:, -k:]
    hits = sum(y_true[i] in topk[i] for i in range(len(y_true)))
    return hits / len(y_true)

