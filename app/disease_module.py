def normalize_symptom(s):
    """
    Normalize one symptom string to a canonical token:
      - lowercase
      - strip spaces/underscores/punctuation
      - map common aliases via CANON_MAP
      - convert spaces to underscores
    Returns None if empty/NaN.
    """
    if pd.isna(s):
        return None
    s = str(s).strip().lower()
    s = s.replace("-", " ").replace("/", " ")
    s = re.sub(r"\s+", " ", s)
    s = s.translate(str.maketrans("", "", string.punctuation))
    s = s.strip()

    # alias mapping (exact)
    if s in CANON_MAP:
        s = CANON_MAP[s]

    # standard format with underscores
    s = s.replace(" ", "_")
    return s if len(s) > 0 else None

